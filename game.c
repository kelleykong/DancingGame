void game_over(void)
{
	int i, j, k, l;
	for(i = 0; i < 50; i++)
	{
		for(j = 0; j < 40; j++)
		{
			for(k = 0; k < 8; k++)
			{
				if(game_o[i][j] & masks[k] == masks[k])
				{
					for(l = 0; l < 4; l++)
						draw_pixel(i * 4 + l, j * 8 + 7 - k, 40);
				}
			}
		}
	}
}

